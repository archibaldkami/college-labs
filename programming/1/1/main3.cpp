    // для обов'язкового друку знака чисел:
    printf("\n%s\n%s\n"," Друк констант цiлого типу за форматом %d "," з одночасним вирiвнюванням по правому краю:"); printf("%d\n %d\n",786,-786); printf("\n%s\n%s\n"," Використання у форматах прапорця (+) "," для обов'язкового друку знака чисел:"); printf("%+d\n %+d\n",786,-786);
