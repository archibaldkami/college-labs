    // з одночасним вирiвнюванням по правому краю:
    printf("\n%s\n%s\n",
    " Друк додатнiх цiлих чисел за форматом %4d",
    " з одночасним вирiвнюванням по правому краю:");
    printf(" %4d\n",1);
    printf(" %4d\n",12);
    printf(" %4d\n",123);
    printf(" %4d\n",1234);
    printf(" %4d\n",12345);
    printf("%s\n%s\n",
    " В останньому випадку бракувало ширини формату-",
    " цим пояснюється неякiсне вирiвнювання");
    printf("\n%s\n%s\n",
    " Друк вiд'ємних цiлих чисел за форматом %4d",
    " з одночасним вирiвнюванням по правому краю:");
    printf(" %4d\n",-1);
    printf(" %4d\n",-12);
    printf(" %4d\n",-123);
    printf(" %4d\n",-1234);
    printf(" %4d\n",-12345);
    printf("%s\n%s\n",
    " В останнiх двох випадках бракувало ширини формату -",
    " цим пояснюється неякiсне вирiвнювання");
    getch();
